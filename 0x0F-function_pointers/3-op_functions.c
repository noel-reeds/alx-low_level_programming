#include "3-calc.h"
/**
  *op_add- adds two integers.
  *
  *@b: operand to add.
  *
  *@a: operand to add.
  *
  *Return: Always 0.
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subtracts b from a.
  *
  *@a: operand to subtract.
  *
  *@b: operand to substract.
  *
  *Return: Always 0.
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - multiplies a and b
  *
  *@b: operand to multiply.
  *
  *@a: operand to multiply.
  *
  *Return: Always 0.
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  *op_div - divides a and b.
  *
  *@b: operand to divide.
  *
  *@a: operand to divide
  *
  *Return: Always 0.
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - finds the modulus.
  *
  *@a: operand to find modulus.
  *
  *@b: operand to find modulus.
  *
  *Return: Always 0.
  */
int op_mod(int a, int b)
{
	return (a % b);
}
